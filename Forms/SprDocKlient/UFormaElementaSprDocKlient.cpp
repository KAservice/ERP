//---------------------------------------------------------------------------
bool TFormaElementaSprDocKlient::Init(void)
{
bool result=false;
if (InterfaceMainObject>0)
	{
	//получим соединение
	IConnectionInterface * i_connection;
	InterfaceMainObject->kanQueryInterface(IID_IConnectionInterface,(void**)&i_connection);
	DM_Connection=i_connection->GetFibConnection();
	i_connection->kanRelease();

	//получим интерфейс COM системы для создания новых объектов
	IMainCOMInterface * i_com;
	InterfaceMainObject->kanQueryInterface(IID_IMainCOMInterface,(void**)&i_com);
	InterfaceGlobalCom=i_com->GetCOM();
	i_com->kanRelease();
	}
InterfaceGlobalCom->kanCreateObject("Oberon.DMSprDocKlient.1",IID_IDMSprDocKlient, (void**)&DM);
DM->Init(InterfaceMainObject,InterfaceImpl);

SercxDBTextEdit->DataBinding->DataSource=DM->DataSourceElement;
NumcxDBTextEdit->DataBinding->DataSource=DM->DataSourceElement;
DateVidcxDBDateEdit->DataBinding->DataSource=DM->DataSourceElement;
VidcxDBTextEdit->DataBinding->DataSource=DM->DataSourceElement;
NameDoccxDBComboBox->DataBinding->DataSource=DM->DataSourceElement;




//ПРИВЕЛЕГИИ
DM_Connection->GetPrivDM(GCPRIV_DM_NoModule);
ExecPriv=DM_Connection->ExecPriv;
InsertPriv=DM_Connection->InsertPriv;
EditPriv=DM_Connection->EditPriv;
DeletePriv=DM_Connection->DeletePriv;

result=true;
return result;
}

